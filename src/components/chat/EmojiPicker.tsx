export default function EmojiPicker({ isOpen, onClose, onEmojiSelect, position }: EmojiPickerProps) {
  const [selectedCategory, setSelectedCategory] = useState<keyof typeof emojiCategories>("smileys");
  const pickerRef = useRef<HTMLDivElement>(null);

  // 외부 클릭 시 닫기
  useEffect(() => {
    function handleClickOutside(event: MouseEvent) {
      if (pickerRef.current && !pickerRef.current.contains(event.target as Node)) {
        onClose();
      }
    }

    if (isOpen) {
      document.addEventListener("mousedown", handleClickOutside);
      return () => document.removeEventListener("mousedown", handleClickOutside);
    }
  }, [isOpen, onClose]);

  if (!isOpen) return null;

  const handleEmojiClick = (emoji: string) => {
    onEmojiSelect(emoji);
    onClose();
  };

  const currentEmojis = emojiCategories[selectedCategory];

  return (
    <div
      ref={pickerRef}
      className="fixed z-50 bg-white border border-gray-200 rounded-lg shadow-lg"
      style={{
        width: "216px",
        height: "282px",
        left: position?.x || 0,
        top: position?.y || 0,
        transform: "translateY(-100%)",
      }}
    >
      {/* 상단 자주 사용되는 이모지 */}
      <div className="p-2 border-b border-gray-100">
        <div className="flex gap-1">
          {frequentEmojis.map((emoji, index) => (
            <button
              key={index}
              onClick={() => handleEmojiClick(emoji)}
              className="w-8 h-8 flex items-center justify-center hover:bg-gray-100 rounded text-lg"
            >
              {emoji}
            </button>
          ))}
          <button
            onClick={onClose}
            className="w-8 h-8 flex items-center justify-center hover:bg-gray-100 rounded text-gray-500"
          >
            ↑
          </button>
        </div>
      </div>

      {/* 카테고리 탭 */}
      <div className="p-2 border-b border-gray-100">
        <div className="flex gap-1">
          {Object.keys(emojiCategories).map((category) => (
            <button
              key={category}
              onClick={() => setSelectedCategory(category as keyof typeof emojiCategories)}
              className={`px-2 py-1 text-xs rounded ${
                selectedCategory === category
                  ? "bg-gray-200 text-gray-800"
                  : "text-gray-600 hover:bg-gray-100"
              }`}
            >
              {category === "smileys" && "😀"}
              {category === "gestures" && "👋"}
              {category === "people" && "👶"}
              {category === "animals" && "🐶"}
              {category === "food" && "🍎"}
              {category === "activities" && "⚽"}
            </button>
          ))}
        </div>
      </div>

      {/* 이모지 그리드 */}
      <div className="p-2 h-[200px] overflow-y-auto">
        <div className="grid grid-cols-6 gap-1">
          {currentEmojis.map((emoji, index) => (
            <button
              key={index}
              onClick={() => handleEmojiClick(emoji)}
              className="w-8 h-8 flex items-center justify-center hover:bg-gray-100 rounded text-lg"
            >
              {emoji}
            </button>
          ))}
        </div>
      </div>
    </div>
  );
}
